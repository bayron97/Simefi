package main.model;

public class garantias {
	private int procesoCompra, tiempo, tiempoManifiesto;
	private String tipoGarantia, tipoManifiesto, unidadTiempo, porcentaje;

	public int getProcesoCompra() {
		return procesoCompra;
	}

	public void setProcesoCompra(int procesoCompra) {
		this.procesoCompra = procesoCompra;
	}

	public int getTiempo() {
		return tiempo;
	}

	public void setTiempo(int tiempo) {
		this.tiempo = tiempo;
	}

	public String getTipoGarantia() {
		return tipoGarantia;
	}

	public void setTipoGarantia(String tipoGarantia) {
		this.tipoGarantia = tipoGarantia;
	}

	public String getTipoManifiesto() {
		return tipoManifiesto;
	}

	public void setTipoManifiesto(String tipoManifiesto) {
		this.tipoManifiesto = tipoManifiesto;
	}

	public String getUnidadTiempo() {
		return unidadTiempo;
	}

	public void setUnidadTiempo(String unidadTiempo) {
		this.unidadTiempo = unidadTiempo;
	}

	public String getPorcentaje() {
		return porcentaje;
	}

	public void setPorcentaje(String porcentaje) {
		this.porcentaje = porcentaje;
	}

	public int getTiempoManifiesto() {
		return tiempoManifiesto;
	}

	public void setTiempoManifiesto(int tiempoManifiesto) {
		this.tiempoManifiesto = tiempoManifiesto;
	}
}
