package main.model;

public class CriterioEvaluacionGeneral {
	private int cod,id, idC;
	private Boolean estado;

	public int getid() {
		return id;
	}

	public void setid(int id) {
		this.id = id;
	}
	
	public int getidC() {
		return idC;
	}

	public void setidC(int idC) {
		this.idC = idC;
	}
	
	public int getCod() {
		return cod;
	}

	public void setCod(int cod) {
		this.cod = cod;
	}
	
	public Boolean getEstado() {
		return estado;
	}

	public void setEstado(Boolean estado) {
		this.estado = estado;
	}

}
