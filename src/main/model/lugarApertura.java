package main.model;

public class lugarApertura {
	private int no;
	private String lugarApertura, estado, observaciones, motivo;

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getLugarApertura() {
		return lugarApertura;
	}

	public void setLugarApertura(String lugarApertura) {
		this.lugarApertura = lugarApertura;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public String getObservaciones() {
		return observaciones;
	}

	public void setObservaciones(String observaciones) {
		this.observaciones = observaciones;
	}
	
	public String getmotivo() {
		return motivo;
	}

	public void setmotivo(String motivo) {
		this.motivo = motivo;
	}
}
