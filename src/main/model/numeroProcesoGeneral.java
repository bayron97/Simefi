package main.model;

public class numeroProcesoGeneral {
	private String cod, estado;
	private int idProc, idincremental;

	public String getCod() {
		return cod;
	}

	public void setCod(String cod) {
		this.cod = cod;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public int getIdProc() {
		return idProc;
	}

	public void setIdProc(int idProc) {
		this.idProc = idProc;
	}

	public int getIdIncremental() {
		return idincremental;
	}

	public void setIdIncremental(int idincremental) {
		this.idincremental = idincremental;
	}
}
