package main.model;

public class beneficiarioMenorFinal {
	private int no;
	private String beneficiarioFinal, direccion, estado, observaciones;

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getBeneficiarioFinal() {
		return beneficiarioFinal;
	}

	public void setBeneficiarioFinal(String beneficiarioFinal) {
		this.beneficiarioFinal = beneficiarioFinal;
	}

	public String getDireccion() {
		return direccion;
	}

	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public String getObservaciones() {
		return observaciones;
	}

	public void setObservaciones(String observaciones) {
		this.observaciones = observaciones;
	}
}
